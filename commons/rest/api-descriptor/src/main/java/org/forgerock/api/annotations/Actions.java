package org.forgerock.api.annotations;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Declare an array of {@link Action} operations from a single method. Typically this would be used when implementing
 * the {@code CollectionResourceProvider} and {@code SingletonResourceProvider}, where all action operations arrive at
 * a single interface method.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.METHOD)
public @interface Actions {
    /**
     * The actions.
     */
    Action[] value();
}
