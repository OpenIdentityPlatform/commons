package org.forgerock.audit.handlers.jdbc;

/**
 * An object capable of rendering an entire SQL statement or a clause.
 *
 * @param <S> the type of object to render the SQL
 */
interface SqlRenderer<S> {
    /**
     * Render the SQL.
     *
     * @return the SQL object
     */
    S toSql();
}